#include "ft_printf.h"

int	ft_get_type_base(char t)
{
	int	base;

	if (t == 'o' || t == 'O')
		base = 8;
	else if (t == 'x' || t == 'X')
		base = 16;
	else
		base = 10;
	return (base);
}
